//4. Bucles (for-loop):
// Escribe un programa que imprima los números del 1 al 10 en la consola.


// Declaramos una variable 
var numeros = 1;

// Iniciamos un bucle 'while' que se ejecutará mientras 'numeros' sea menor que 11
while (numeros < 11) {
    // Imprimimos en la consola el valor actual de 'numeros'
    console.log('NUMERO:', numeros);
    
    // Incrementamos la variable 'numeros' 
    numeros++;
}
