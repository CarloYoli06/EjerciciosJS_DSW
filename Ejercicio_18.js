// 1️8️. Ordenamiento de un array:
// Dado let palabras = ["banana", "manzana", "pera", "uva"], usa .sort() para ordenarlas alfabéticamente.

// Se crea el arreglo de las palabras
let palabras = ["banana", "manzana", "pera", "uva"];
//Se ordena de manera alfabetica
palabras.sort();

//Se imprime el arreglo para comprobar que se ordena
console.log(palabras);